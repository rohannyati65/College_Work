"""Assign value of right side of expression to left 
side operand"""
x = 5
print(x)


"""Add AND: Add right side operand with left side 
operand and then assign to left operand  x=x+y"""
x = 5
y=3
x += y
print(x)


"""Subtract AND: Subtract right operand from left
 operand and then assign to left operand   x=x-y"""

x = 5
y=3
x -= y
print(x)



"""Multiply AND: Multiply right operand with left
 operand and then assign to left operand   x=x*y"""
x = 5
y=3
x *= y
print(x)




"""Divide AND: Divide left operand with right operand
 and then assign to left operand    x=x/y"""
 
x = 5
y=3
x /= y
print(x)



"""Modulus AND: Takes modulus using left and right
 operands and assign result to left operand  x=x%y"""
x = 5
y=3
x%=y
print(x)


"""Divide(floor) AND: Divide left operand with 
right operand and then assign the value(floor) to 
left operand   x=x//y"""
x = 5
y=3
x//=y
print(x)


"""Exponent AND: Calculate exponent(raise power) 
value using operands and assign value to left
 operand   x=x**y"""
x = 5
y=3
x **= y
print(x)



"""Performs Bitwise AND on operands and assign value 
to left operand   x=x&y"""
x = 5
y=3
x &= y
print(x)


"""Performs Bitwise OR on operands and assign value
 to left operand   x=x|y"""
x = 5
y=3
x |= y
print(x)


"""Performs Bitwise xOR on operands and assign value 
to left operand   x=x^y"""
x = 5
y=3
x ^= y
print(x)


"""Performs Bitwise right shift on operands and 
assign value to left operand   x=x>>y"""
x = 5
y=3
x >>= y
print(x)

"""Performs Bitwise left shift on operands and 
assign value to left operand   x=x<<y"""

x = 5
y=3
x <<= y
print(x)

